    def computeValueFromQValues(self, state):
        """
          Returns max_action Q(state,action)
          where the max is over legal actions.  Note that if
          there are no legal actions, which is the case at the
          terminal state, you should return a value of 0.0.
        """
        "*** YOUR CODE HERE ***"
        ql=self.getLegalActions(state)#獲得當前state合法的Action
        if len(ql)==0:#檢查合法的Action是否為空
          return 0.0
        qv=[]#用來儲存Qvalue
        for i in ql:
          qv.append(self.getQValue(state,i))#將getQvalue執行後的所有Qvalue全部儲存起來
        mx=max(qv)#找出Qvalue中最大的
        return mx
        util.raiseNotDefined()

    def computeActionFromQValues(self, state):
        """
          Compute the best action to take in a state.  Note that if there
          are no legal actions, which is the case at the terminal state,
          you should return None.
        """
        "*** YOUR CODE HERE ***"
        ql=self.getLegalActions(state)#獲得當前state合法的Action
        if len(ql)==0:#檢查合法的Action是否為空
          return None
        st=[]#儲存Action
        mx=self.getQValue(state,ql[0])#將第一個action執行getQvalue的值設為max
        for i in ql:
          if len(st)==0:#將第一個Action先儲存起來
            st.append(i)
          else:
            if self.getQValue(state,i)>mx:#如果該Action的Qvalue比max大
              st.clear()#將原本的action清空
              st.append(i)#儲存新的action
              mx=self.getQValue(state,i)#更新max
            elif self.getQValue(state,i)==mx:#如果該Action的Qvalue跟max一樣大
              st.append(i)#增加action
        if len(st)==1:#如果只有一個最佳action
          return st[0]#回傳最佳action
        else:#如果不只一個最佳action
          return random.choice(st)#隨機回傳一個最佳action
        util.raiseNotDefined()

