    def computeValueFromQValues(self, state):
        """
          Returns max_action Q(state,action)
          where the max is over legal actions.  Note that if
          there are no legal actions, which is the case at the
          terminal state, you should return a value of 0.0.
        """
        "*** YOUR CODE HERE ***"
        legalActions = self.getLegalActions(state)  #先get legalAction
        if len(legalActions)==0:                    # if legalAction 的 matrix 長度為0 代表沒有legalAction
          return 0.0                                # 沒有legalAction的話 return 0.0
        tmp = util.Counter()                        # 先宣告一個tmp容器
        for action in legalActions:                 #尋找所有在legalAction裡的Action
          tmp[action] = self.getQValue(state, action)   #把Action對應到的value 存取到tmp
        return tmp[tmp.argMax()]                        #return max in tmp
