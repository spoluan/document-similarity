    def computeValueFromQValues(self, state):
        """
          Returns max_action Q(state,action)
          where the max is over legal actions.  Note that if
          there are no legal actions, which is the case at the
          terminal state, you should return a value of 0.0.
        """
        "*** YOUR CODE HERE ***"
        legalActions = self.getLegalActions(state) # 先取得所有可以執行的action選項
        if len(legalActions) == 0:
            return 0.0 # 如果沒有可執行的action，就直接回傳0
        tmp = util.Counter()
        for action in legalActions: # 取得此state所有可能action的Q-value值
            tmp[action] = self.getQValue(state, action)
        return tmp[tmp.argMax()] # 回傳其中最大的Q-value作為此state的value值
        util.raiseNotDefined()

    def computeActionFromQValues(self, state):
        """
          Compute the best action to take in a state.  Note that if there
          are no legal actions, which is the case at the terminal state,
          you should return None.
        """
        "*** YOUR CODE HERE ***"
        legalActions = self.getLegalActions(state) # 先取得所有可以執行的action選項
        best_action = None
        max_val = float('-inf')
        for action in legalActions:
            qVal = self.getQValue(state, action) # 取得此state所有可能action的Q-value值
            if max_val < qVal: # 如果取得的Q-value比現在目前的大
                max_val = qVal # 就將其設為max
                best_action = action # 並將action設為最好的
        return best_action # 最後return最好的action
        util.raiseNotDefined()

